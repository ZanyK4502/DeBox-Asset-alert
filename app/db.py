def list_due_scheduled_subscriptions(hours: int = 24) -> list[dict]:
    with connect() as connection:
        expire_active_subscriptions(connection)
        rows = connection.execute(
            """
            SELECT * FROM subscriptions
            WHERE status = 'active'
              AND plan_code IN ('standard', 'professional')
              AND expires_at > CURRENT_TIMESTAMP
              AND (
                scheduled_push_last_sent_at IS NULL
                OR scheduled_push_last_sent_at <= CURRENT_TIMESTAMP - (%s * INTERVAL '1 hour')
              )
            ORDER BY scheduled_push_last_sent_at NULLS FIRST, id
            """,
            (hours,),
        ).fetchall()
    return [dict(row) for row in rows]


def mark_scheduled_push_sent(subscription_id: int) -> None:
    with connect() as connection:
        connection.execute(
            """
            UPDATE subscriptions
            SET scheduled_push_last_sent_at = CURRENT_TIMESTAMP
            WHERE id = %s
            """,
            (subscription_id,),
        )
