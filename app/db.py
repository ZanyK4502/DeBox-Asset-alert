from __future__ import annotations

import psycopg
from psycopg.rows import dict_row

from app.config import settings


SCHEMA = """
CREATE TABLE IF NOT EXISTS watch_rules (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    debox_user_id TEXT NOT NULL,
    chain_key TEXT NOT NULL DEFAULT 'bsc',
    chain_id INTEGER NOT NULL,
    wallet_address TEXT NOT NULL,
    token_address TEXT,
    rule_type TEXT NOT NULL,
    threshold TEXT NOT NULL,
    notification_chat_id TEXT NOT NULL,
    notification_chat_type TEXT NOT NULL,
    notification_label TEXT NOT NULL DEFAULT '',
    enabled INTEGER NOT NULL DEFAULT 1,
    last_value TEXT,
    last_checked_at TIMESTAMPTZ,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS subscriptions (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    debox_user_id TEXT NOT NULL,
    plan_code TEXT NOT NULL,
    status TEXT NOT NULL,
    starts_at TIMESTAMPTZ,
    expires_at TIMESTAMPTZ,
    scheduled_push_last_sent_at TIMESTAMPTZ,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS orders (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    debox_user_id TEXT NOT NULL,
    payer_address TEXT,
    chain_key TEXT NOT NULL DEFAULT 'bsc',
    chain_id INTEGER NOT NULL,
    token_address TEXT,
    recipient_address TEXT,
    payment_contract_address TEXT,
    total_amount TEXT NOT NULL,
    plan_code TEXT NOT NULL DEFAULT 'standard',
    tx_hash TEXT,
    status TEXT NOT NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS alert_events (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    watch_rule_id INTEGER NOT NULL REFERENCES watch_rules(id),
    event_type TEXT NOT NULL,
    previous_value TEXT,
    current_value TEXT,
    notification_message_id TEXT,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS notification_groups (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    debox_user_id TEXT NOT NULL,
    gid TEXT NOT NULL,
    name TEXT NOT NULL DEFAULT '',
    enabled INTEGER NOT NULL DEFAULT 1,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    UNIQUE (debox_user_id, gid)
);
"""

MIGRATIONS = [
    "ALTER TABLE watch_rules ADD COLUMN IF NOT EXISTS chain_key TEXT NOT NULL DEFAULT 'bsc'",
    "ALTER TABLE watch_rules ADD COLUMN IF NOT EXISTS notification_label TEXT NOT NULL DEFAULT ''",
    "ALTER TABLE orders ADD COLUMN IF NOT EXISTS chain_key TEXT NOT NULL DEFAULT 'bsc'",
    "ALTER TABLE subscriptions ADD COLUMN IF NOT EXISTS scheduled_push_last_sent_at TIMESTAMPTZ",
]


def connect() -> psycopg.Connection:
    if not settings.database_url:
        raise RuntimeError("DATABASE_URL is required. Configure PostgreSQL before starting the app.")
    return psycopg.connect(settings.database_url, row_factory=dict_row)


def fetch_inserted(connection: psycopg.Connection, cursor, table: str) -> dict:
    row_id = cursor.fetchone()["id"]
    row = connection.execute(f"SELECT * FROM {table} WHERE id = %s", (row_id,)).fetchone()
    return dict(row)


def initialize_database() -> None:
    with connect() as connection:
        for statement in SCHEMA.split(";"):
            statement = statement.strip()
            if statement:
                connection.execute(statement)
        for statement in MIGRATIONS:
            connection.execute(statement)


def expire_pending_orders(minutes: int = 30, expire_all: bool = False) -> int:
    with connect() as connection:
        if expire_all:
            cursor = connection.execute("UPDATE orders SET status = 'expired' WHERE status = 'pending'")
        else:
            cursor = connection.execute(
                """
                UPDATE orders
                SET status = 'expired'
                WHERE status = 'pending'
                  AND created_at <= CURRENT_TIMESTAMP - (%s * INTERVAL '1 minute')
                """,
                (minutes,),
            )
        return cursor.rowcount


def create_watch_rule(
    debox_user_id: str,
    chain_key: str,
    chain_id: int,
    wallet_address: str,
    token_address: str | None,
    rule_type: str,
    threshold: str,
    notification_chat_id: str,
    notification_chat_type: str,
    notification_label: str = "",
) -> dict:
    with connect() as connection:
        cursor = connection.execute(
            """
            INSERT INTO watch_rules (
                debox_user_id, chain_key, chain_id, wallet_address, token_address,
                rule_type, threshold, notification_chat_id, notification_chat_type,
                notification_label
            ) VALUES (%s, %s, %s, %s, %s, %s, %s, %s, %s, %s)
            RETURNING id
            """,
            (
                debox_user_id,
                chain_key,
                chain_id,
                wallet_address,
                token_address,
                rule_type,
                threshold,
                notification_chat_id,
                notification_chat_type,
                notification_label,
            ),
        )
        return fetch_inserted(connection, cursor, "watch_rules")


