def complete_order(order_id: int, tx_hash: str) -> dict:
    with connect() as connection:
        connection.execute(
            """
            UPDATE orders
            SET tx_hash = %s, status = 'paid'
            WHERE id = %s AND status = 'pending'
            """,
            (tx_hash, order_id),
        )
        row = connection.execute("SELECT * FROM orders WHERE id = %s", (order_id,)).fetchone()
    return dict(row)


def expire_active_subscriptions(connection: psycopg.Connection, debox_user_id: str | None = None) -> None:
    if debox_user_id:
        connection.execute(
            """
            UPDATE subscriptions
            SET status = 'expired'
            WHERE debox_user_id = %s AND status = 'active'
              AND expires_at <= CURRENT_TIMESTAMP
            """,
            (debox_user_id,),
        )
    else:
        connection.execute(
            """
            UPDATE subscriptions
            SET status = 'expired'
            WHERE status = 'active' AND expires_at <= CURRENT_TIMESTAMP
            """
        )


def activate_subscription(debox_user_id: str, plan_code: str, days: int) -> dict:
    with connect() as connection:
        expire_active_subscriptions(connection, debox_user_id)
        active = connection.execute(
            """
            SELECT * FROM subscriptions
            WHERE debox_user_id = %s AND status = 'active'
              AND expires_at > CURRENT_TIMESTAMP
            ORDER BY id DESC
            LIMIT 1
            """,
            (debox_user_id,),
        ).fetchone()

        if active and active["plan_code"] == plan_code:
            row = connection.execute(
                """
                UPDATE subscriptions
                SET expires_at = expires_at + (%s * INTERVAL '1 day')
                WHERE id = %s
                RETURNING *
                """,
                (days, active["id"]),
            ).fetchone()
            return dict(row)

        if active and active["plan_code"] == "free" and plan_code != "free":
            connection.execute("UPDATE subscriptions SET status = 'upgraded' WHERE id = %s", (active["id"],))
        elif active and active["plan_code"] != plan_code:
            raise ValueError("当前订阅未到期，暂时不能切换套餐；同套餐可以提前续费。")

        cursor = connection.execute(
            """
            INSERT INTO subscriptions (
                debox_user_id, plan_code, status, starts_at, expires_at
            ) VALUES (
                %s, %s, 'active', CURRENT_TIMESTAMP,
                CURRENT_TIMESTAMP + (%s * INTERVAL '1 day')
            )
            RETURNING id
            """,
            (debox_user_id, plan_code, days),
        )
        return fetch_inserted(connection, cursor, "subscriptions")


def get_active_subscription(debox_user_id: str) -> dict | None:
    with connect() as connection:
        expire_active_subscriptions(connection, debox_user_id)
        row = connection.execute(
            """
            SELECT * FROM subscriptions
            WHERE debox_user_id = %s AND status = 'active'
              AND expires_at > CURRENT_TIMESTAMP
            ORDER BY id DESC
            LIMIT 1
            """,
            (debox_user_id,),
        ).fetchone()
    return dict(row) if row else None


def has_used_plan(debox_user_id: str, plan_code: str) -> bool:
    with connect() as connection:
        row = connection.execute(
            """
            SELECT 1 FROM subscriptions
            WHERE debox_user_id = %s AND plan_code = %s
            LIMIT 1
            """,
            (debox_user_id, plan_code),
        ).fetchone()
    return row is not None


def create_notification_group(debox_user_id: str, gid: str, name: str = "") -> dict:
    with connect() as connection:
        row = connection.execute(
            """
            INSERT INTO notification_groups (debox_user_id, gid, name, enabled)
            VALUES (%s, %s, %s, 1)
            ON CONFLICT (debox_user_id, gid)
            DO UPDATE SET name = EXCLUDED.name, enabled = 1
            RETURNING *
            """,
            (debox_user_id, gid, name),
        ).fetchone()
    return dict(row)


